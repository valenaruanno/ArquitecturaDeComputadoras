ORG 3000H 
MUL: MOV CL, 0 
MOV AL, 6 
LAZO: ADD CL, AH 
DEC AL
JNZ LAZO 
RET 

ORG 2000H 
MOV BX, OFFSET CAD 
MOV AL, OFFSET FINCAD - OFFSET CAD 
INT 7 
MOV DL, 0 
LOOP: MOV BX, OFFSET NUM 
INT 6 
MOV BX, OFFSET NROS 
MOV AX, 00 
MOV AH, NUM 
CMP AH, 30H 
JNZ RESET 
INC DL 
JMP SIGUE 
RESET: MOV DL, 0
SIGUE: SUB AH, 30H 
MOV CX, 00
CALL MUL 
ADD BX, CX 
MOV AL, 6 
INT 7 
CMP DL, 2 
JZ FIN 
JMP LOOP 
FIN: INT 0
END 
