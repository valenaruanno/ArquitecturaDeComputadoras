ORG 1000H 
MSJ DB "INGRESE UN NUMERO"
FIN DB ? 
NONUM DB "CARACTER NO VALIDO" 
FINNO DB ? 

ORG 1500H
ES_NUM: CMP BYTE PTR[BX], 30H 
JS NOVALIDO 
MOV AH, 0FFH 
JMP FINN 
NOVALIDO: MOV AH, 00H 
MOV BX, OFFSET NONUM
MOV AL, OFFSET FINNO - OFFSET NONUM 
INT 7 
FINN: RET 

ORG 2000H 
MOV BX, OFFSET MSJ 
MOV AL, OFFSET FIN - OFFSET MSJ 
INT 7 
MOV BX, OFFSET NUM 
INT 6 
PUSH BX 
MOV BX, OFFSET NUM 
CALL ES_NUM 
POP BX 
MOV AL, 1 
INT 7 
MOV CL, NUM 
INT 0 
END 